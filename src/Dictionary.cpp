// ────────────────────────────────────────────────────────────────────────────
//  isValid  –  upper-cases the word then looks it up
// ────────────────────────────────────────────────────────────────────────────
bool Dictionary::isValid(const std::string& word) const {
    if (static_cast<int>(word.size()) < MIN_WORD_LENGTH) return false;

    std::string upper = word;
    std::transform(upper.begin(), upper.end(), upper.begin(), ::toupper);
    return m_words.count(upper) > 0;
}
